#ifndef SPI_H
#define SPI_H

/****************************************************************************
SPI initialization (as a Master)
****************************************************************************/
void SPI_init(void);

/****************************************************************************
Send a message to slave through SPI comm
****************************************************************************/
void SPI_write(char cData);

/****************************************************************************
Read a message from slave through SPI comm
****************************************************************************/
char SPI_read(void);

/****************************************************************************
Select CAN-controller (~CS=0)
****************************************************************************/
void SPI_select(void);

/****************************************************************************
Deselect CAN-controller (~CS=1)
****************************************************************************/
void SPI_deselect(void);

#endif // SPI_H